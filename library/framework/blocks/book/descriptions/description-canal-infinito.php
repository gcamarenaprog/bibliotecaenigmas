    <h1 class="text-center">Un canal que se convirtió en parte de nuestra cultura</h1>

    <p>Nacido en los años 90 y llegando a México a principios del 2000 con la señal de DIRECTV y SKY México en 2001, se
      recuerda con nostalgia las primeras transmisiones, sobre todo por los programas filmados en territorio
      mexicano.</p>

    <p>Muchos de sus programas están alrededor de temas como civilizaciones azteca y maya, los permanentes avistamientos
      de ovnis en el país, las enseñanzas de don Juan, el Día de los Muertos, nahuales y las prácticas chamánicas, entre
      otros. Con una programación variada y novedosa, el canal se convirtió en un refugio para aquellos que buscaban
      algo más allá de lo convencional.</p>

    <p>Una programación las 24 h, con una temática única en la historia de la televisión latinoamericana y quizás
      mundial, donde la superstición, la magia, el esoterismo y las nuevas ciencias eran el eje central de este
      canal.</p>

    <h1 class="text-center">Un estilo único</h1>
    <p>El Canal Infinito tenía un estilo propio que lo diferenciaba de otros canales de documentales. Su enfoque en
      temas esotéricos, paranormales y holísticos lo convirtió en un destino obligado para aquellos que buscaban
      respuestas a preguntas que no tenían respuesta. Programas como «Infinito Vivo», «La Brújula» y «Misterios Urbanos»
      se convirtieron en favoritos de muchos.</p>

    <h1 class="text-center">Algo diferente</h1>
    <p>Fue un canal argentino que terminó transmitiéndose en muchos países de Latinoamérica y que poco tardó en
      convertirse en el favorito de la población «no convencional»; único en su formato y estilo, era de agrado para una
      audiencia que buscaba algo diferente a la televisión convencional.</p>

    <p>A lo largo de sus años, el eslogan del canal fue cambiando, pasando por:</p>

    <p>
    <ul>
      <li>1994-1995: <i>24 horas de documentales</i>.</li>
      <li>1995-1996: <i>El primer canal latinoamericano de documentales</i>.</li>
      <li>1997: <i>Señal del nuevo milenio</i>.</li>
      <li>1998: <i>Experimenta lo desconocido</i>.</li>
      <li>1999-2003: <i>Existe otra realidad y solo un canal te la muestra / Existe otra realidad</i>.</li>
      <li>2004-2008: <i>Abre tu mente ∴</i></li>
      <li>2009-2015: <i>Realidad que supera la ficción</i>.</li>
    </ul>
    </p>

    <h1 class="text-center">Muerte del canal</h1>
    <p>
      A partir de 2009, para sorpresa de la audiencia y bajo el slogan «Realidad que supera a la ficción», la temática
      abordada por la señal cambió radicalmente, al igual que todos sus contenidos, hasta que el 10 de marzo de 2015
      dejó de transmitirse. </p>

    <p>Dejando un vacío en una gran audiencia en toda Latinoamérica, marcando un antes y un después en la temática de lo
      misterioso y oculto de nuestro mundo, rompiendo esquemas, moldes, contenidos, etc. </p>

    <p>Nos hizo soñar, pensar, abrir nuestra mente y acercarnos a lo desconocido de una manera que no se ha vuelto a ver
      en la historia de la televisión latinoamericana.
    </p>

    <h1 class="text-center">Un legado que perdura</h1>
    <p>Aunque el canal cambió de manos y eventualmente se apagó en 2015, su legado sigue vivo en la memoria de muchos.
      La nostalgia por el Canal Infinito es un testimonio de su impacto en la cultura y la vida de las personas en
      Latinoamerica. Muchos aún anhelan su regreso en el mismo formato que lo hizo tan especial, ya que hizo énfasis en
      la producción de documentales sobre esoterismo, las ciencias ocultas, los hechos paranormales, los OVNIS y todo lo
      vinculado a las terapias complementarias y las prácticas holísticas.</p>

    <p>Producciones de calidad: informes, referencias, investigaciones y entrevistas a personas normales en el lugar de
      los hechos, investigaciones en sitio, sin comerciales, sin censura y en un lenguaje natural y abierto; nunca lo
      olvidaremos.</p>

    <p>Haciendo hincapié en la revolución que causó en la audiencia de los 90s y principios del nuevo milenio sobre la
      espiritualidad de la gente, haciéndonos cuestionar sobre nuestra existencia y nuestra realidad.</p>


