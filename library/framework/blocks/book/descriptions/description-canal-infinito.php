  <h3 class="toggle-head">Historia oficial <i class="fa fa-angle-down" aria-hidden="true"></i></h3>
  <div class="toggle-content" style="display: none;">

    <h1 class="text-center">Inicio</h1>

    <p>El canal fue creado y operado originalmente por la empresa argentina Imagen Satelital del empresario argentino
      Alberto González, en febrero de 1994. En una primera etapa, el canal intentó ganar un espacio dentro de las
      señales del género documentalista, sin grandes diferencias conceptuales en términos de su programación.</p>

    <p>En 1997, Imagen Satelital y su paquete de señales fueron adquiridos por Cisneros Television Group (que en el año
      2000 pasó a llamarse Claxson Interactive Group al fusionarse con Ibero American Media Partners y el portal El
      Sitio). Para septiembre de ese año, el canal empezó a potenciar el ámbito de las producciones propias, con el
      ciclo Noches temáticas y, de cara a la Copa Mundial de 1998, Esto es Fútbol, acerca de la historia del torneo. En
      septiembre de 1998, con el término del programa infantil Recreo Satelital, Infinito pasa a programar ciclos de
      documentales en el horario de tarde, convirtiéndose en octubre en la primera empresa latinoamericana con un edit
      box platinum.</p>

    <h1 class="text-center">Relanzamiento</h1>

    <p>Frente a la necesidad de reposicionar el canal y ubicarlo dentro de las marcas líderes, el nuevo propietario del
      canal reformuló el contenido de la programación y realiza el relanzamiento de la señal el 13 de abril de 1999. En
      esta nueva etapa, el canal mantiene el formato de documentales, pero apuntando a las temáticas que sorprenden e
      intrigan, que escapan de lo normal, hechos paranormales sin explicación, temas tabú y realidades alternativas que
      perduran y se potencian en el nuevo milenio.</p>

    <p>Frente a esos cambios, Infinito compró programas paranormales de Televisión Nacional de Chile, que ya había
      vendido al canal cuando su programación era de índole documental. En su primer año como canal paranormal, Infinito
      tenía dos programas de producción propia acorde a la nueva temática, Zona Infinito y Signos. Con el relanzamiento
      del canal, Infinito era recibido por 10.415.000 personas en Latinoamérica, oscilando entre los 12 y 64 años de
      edad. Una gran parte de su audiencia (8.087.000 personas) se encontraba en Argentina.</p>

    <p>A fin de potenciar su audiencia en México, en abril del 2000 se tomó la decisión de filmar programas de Zona
      Infinito en el país, luego de la llegada de la señal a DIRECTV a finales del año anterior, luego de hacer una
      experiencia en Chile. Para julio se estrena la serie First Wave de manera exclusiva, en la nueva franja Infinito
      Films & Series. La audiencia del canal incrementó en Brasil, potenciando la creación de programas originales para
      el país, además de acuerdos con productoras independientes de varios países. En diciembre, la productora
      colombiana Televideo terminó la producción de un documental de dos partes dedicado a la caída del general Noriega
      de Panamá, enviada a Infinito, con plazo de emisión para enero del 2001.</p>

    <p>Durante el año 2001, Infinito se abre a una nueva apuesta y crea un nuevo espacio dentro de su programación para
      la producción original, uno de los pilares diferenciales de la marca, teniendo un 25 % de producción propia dentro
      del total de programas ofrecidos. A lo largo de ese año, Infinito incrementa su presencia en México, logrando
      acuerdos con Sky (600 mil suscriptores), los 298 sistemas representantes de PCTV (1.450.000 suscriptores) y
      Cablevisión en Distrito Federal (9 millones de suscriptores en toda la región). A partir de agosto de ese año, los
      documentales originales de la franja Hora Infinito pasaron a emitirse por televisión abierta en Canal 7 de
      Argentina, con el intuito de fortalecer la marca y brindar contenido inédito a audiencias de señal abierta. En el
      MIPDOC de 2002 en Cannes, se demostró el potencial de vender Hora Infinito a más televisoras abiertas, por los
      temas llamativos de sus producciones. Con eso, Claxson, con su catálogo de más de 400 horas de documentales
      propios, lograría vender la franja en un periodo de 52 semanas (un documental por semana) a televisoras
      interesadas.</p>

    <p>A partir de octubre de 2001, Infinito se divide en dos señales, Infinito Sur para Argentina, Paraguay y Uruguay e
      Infinito Latinoamérica para el resto de la región.</p>

    <p>En 2002, Infinito presentó el programa Mundo Infinito, presentado por el actor puertorriqueño Walter Mercado, y
      grabado en las instalaciones del Hotel Ceasar's Palace en Las Vegas. Con el anuncio del programa en junio de ese
      año de cara a un posterior evento en Miami el 31 de julio, se grabaron ocho programas, con la previsión de grabar
      cuatro programas por mes. Ese mismo año, el canal contaba con dos millones de abonados en México, en un país donde
      la temática paranormal cuadraba bien con la realidad cultural del país. Para fidelizar al público, la agencia
      creativa de Claxson InJaus anunció una campaña para lanzar el CD Música Infinito como un premio novedoso a los
      operadores de cable, para seguir ofertando el canal.</p>

    <p>De cara a estrategias de Claxson para la temporada 2002/2003, se fijó como meta de la empresa proveer a Infinito
      el 90 % de las producciones propias del grupo, el restante estando reservado a las señales Space y Venus. Para
      cumplir tales prioridades, los mercados que mostraban más potencial eran México y la península ibérica (donde para
      esta última estaba prevista su entrada para el año 2003). En diciembre de 2002, Infinito ya estaba apuntado a
      lanzarse en el mercado hispano de Estados Unidos, lo cual fue alcanzado en abril de 2003 en el paquete hispano de
      Time Warner Cable en el head-end de Nueva York, alcanzando una audiencia potencial de 1.2 millones de latinos. En
      septiembre de ese año, el canal llega a la competencia Cablevision, quien firmó un acuerdo con Claxson para dos
      señales para fortalecer el paquete iO en español, HTV e Infinito. Finalmente, en Argentina, se firmó un acuerdo
      con Kier para lanzar la serie de libros Colección Infinito.</p>

    <h1 class="text-center">Segundo relanzamiento, etapa Abre tu mente, compra por Turner</h1>

    <p>En mayo de 2006, el canal entra a Cable Mágico de Perú y ya tenía un contrato vigente con TV Cabo de Portugal,
      que lo iba a incorporar a partir de julio de ese año.

    <p>En octubre de 2007, Infinito junto con un paquete de otras 6 señales de Claxson Interactive Group fueron
      adquiridas por Turner Broadcasting System, una de las divisiones de Time Warner (hoy Warner Bros. Discovery).</p>

    <h1 class="text-center">Tercer relanzamiento</h1>

    <p>Desde el 1 de enero de 2009, después de una reestructuración por parte de Turner, el canal abandona la temática y
      el perfil que manejaba y renueva su contenido, cambiando hacia un nuevo enfoque orientado a la inspiración, el
      asombro y las enseñanzas de hechos y personas del mundo real que han superado a lo esperable hasta en la
      ficción.</p>

    <p>Relegando a un segundo plano el formato documental y enfocándose en telerrealidad, series y películas basadas en
      hechos reales; un cambio radical que fue acompañado por una renovación de imagen institucional, estética y
      audiovisual.</p>

    <h1 class="text-center">Cese de emisiones</h1>

    <p>Infinito cerró sus emisiones el 10 de marzo de 2015 en Argentina y el 17 de marzo de 2015 en el resto de
      Latinoamérica, para ser reemplazado por TNT Series. La mayor parte de la programación de Infinito fue trasladada a
      la señal truTV.</p>
